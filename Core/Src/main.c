/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  DBGMCU->APB1FZ |= DBGMCU_APB1_FZ_DBG_IWDG_STOP;
  reg_rcc_csr_copy = RCC->CSR;
  if(!(RCC->CSR & RCC_CSR_LSIRDY))
  {
	  HAL_NVIC_SystemReset();
  }
  while(!(RCC->CR & RCC_CR_PLLRDY));
  RCC->CSR |= RCC_CSR_RMVF;
  if(RCC->CSR & RCC_CSR_RTCEN)
  {
	  RCC->CSR |= RCC_CSR_RTCRST;
  }
  RCC->CSR &= ~RCC_CSR_RTCRST;

  __enable_irq();

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_IWDG_Init();
  MX_GPIO_Init();
  MX_CRC_Init();
  MX_RTC_Init();
  MX_I2C1_Init();
  MX_I2C3_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_USART1_UART_Init();
  MX_USART2_UART_Init();
  MX_USART4_UART_Init();
  /* USER CODE BEGIN 2 */

  /* Sensor initialization*/
  printf("\r\n----------------------------------------------------------\r\n");
  printf("|                         GPS Module                     |\r\n");
  {
	  char str[16]; // str to be destroyed after logging.
	  sprintf(str, "%d.%d.%d.%d", MAJOR_FW_VERSION, MINOR_FW_VERSION, SUB_FW_VERSION, TEST_FW_VERSION);
	  printf("|               Firmware Revision %-22s |\r\n", str);
  }
  printf("----------------------------------------------------------\r\n");

  LogResetReason(reg_rcc_csr_copy);

  Mag_Init();
  Accel_Init();
  Init_GPS();

  const Module_Slave_Bus_t *pbus = Init_Module_Bus(I2C1, INT_OUT_GPIO_Port, INT_OUT_Pin, &FWVer);
  if(pbus)
  {
	printf("GPS Module Bus ready\r\n");
  }
  else
  {
	printf("\r\nFAILED to initialize the GPS Module Bus \r\n");
  }
	led_blink_task();
	led_blink_slow();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	APP_IWDG_Refresh();
	led_blink_task();

	if((MFR_PING_RX == 1 || (!(nTEST_GPIO_Port->IDR & nTEST_Pin) && (Module_Info.Module_Serial_Number[0] != 0)))) // if on yellow board or flag is called
	{
		MFR(); // manufacturing test
	}
	else // if(nTEST_GPIO_Port->IDR & nTEST_Pin) // if(Is_Enumeration_Complete()) // ZGy_Note
	{
		Module_Bus_Tasks();
		Process_Mag_Data();   // Read Mag Data     // ZGy_Note: a patch to give more time to the Module_Bus_Tasks();

		if(Is_Enumeration_Complete() || !(nTEST_GPIO_Port->IDR & nTEST_Pin))
		{
			Process_Accel_Data(); // Read Accel Data   // ZGy_Note a patch to give more time to the Module_Bus_Tasks();
			Module_Bus_Tasks();
			Process_Mag_Data();   // Read Mag Data     // ZGy_Note: a patch to give more time to the Module_Bus_Tasks();

			Module_GPS_Tasks();   //runs Accel,GPS,Mag processes
			Module_Bus_Tasks();
			Process_Mag_Data();   // Read Mag Data     // ZGy_Note: a patch to give more time to the Module_Bus_Tasks();
		}
	}
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

